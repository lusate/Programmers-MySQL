SELECT HISTORY_ID, CAR_ID, 
DATE_FORMAT(START_DATE, '%Y-%m-%d'), 
DATE_FORMAT(END_DATE, '%Y-%m-%d'), 

# DATEDIFF는 START_DATE - END_DATE + 1이개 때문에 30일 이상이 장기 대여면 29로 해줘야 한다.
# 즉 IF(DATEDIFF(END_DATE, START_DATE) + 1 >= 30, '장기 대여', '단기 대여') RENT_TYPE 와 같음.
IF(DATEDIFF(END_DATE, START_DATE) >= 29, '장기 대여', '단기 대여') RENT_TYPE
FROM CAR_RENTAL_COMPANY_RENTAL_HISTORY
WHERE DATE_FORMAT(START_DATE, '%Y-%m') LIKE '2022-09'
ORDER BY HISTORY_ID DESC
